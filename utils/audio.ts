// Simplified audio utils for offline mode

// Simple base64 encoder if needed for other purposes, 
// though strictly not needed for offline Web Speech API
export function arrayBufferToBase64(buffer: ArrayBuffer): string {
  let binary = '';
  const bytes = new Uint8Array(buffer);
  const len = bytes.byteLength;
  for (let i = 0; i < len; i++) {
    binary += String.fromCharCode(bytes[i]);
  }
  return btoa(binary);
}
